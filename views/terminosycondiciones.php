			<h3 class="p-15 text-center titles">CONSTRULITA LIGHTING AWARDS 2018</h3>
			<p class="text-center">Organizado por GRUPO CONSTRULITA</p>
			<h3 class="text-uppercase titles">ELEGIBILIDAD</h3>
			<p>
				<b>Construlita Lighting Awards</b> tiene como objetivo dar reconocimiento al diseño de iluminación en Iberoamérica. Cualquier persona puede registrarse y publicar un proyecto para ser evaluado por los miembros de nuestro jurado, quienes determinan el galardón que reciben los proyectos más destacados. La premiación se llevará a cabo en Expo Lighting America el 28 de febrero de 2018.
			</p>
			<p>El proyecto que se registre debe ser:</p>
			<ol class="ol-roman">
				<li>Una solución de diseño de iluminación arquitectónica permanente cuya construcción se haya terminado después del 1° de enero del 2016, o bien</li>
				<li>Un proyecto conceptual de iluminación, que se proponga para resolver, mejorar o rescatar algún espacio interior o exterior existente.</li>
			</ol>
			<p>
				Todos los participantes deberán proveer sus datos completos de registro y llevar a cabo la publicación del proyecto a través de nuestro <a href="http://awards.construlitalighting.com/cla" target="_blank">sitio</a> antes de la fecha límite el 31 de enero de 2018. Los proyectos presentados no deberán haber participado en ninguna edición anterior de <b>Construlita Lighting Awards</b>.
			</p>
			<p>
				Al concursar en esta convocatoria, los participantes declaran que los proyectos son de su completa autoría y que poseen los derechos para autorizar la distribución y publicación de los materiales relacionados con ellos.
			</p>

			<h3 class="text-uppercase titles">CATEGORÍAS</h3>

			<p>Los participantes deberán definir en qué categoría desean inscribir cada proyecto:</p>
			<br>
			<p>PROYECTOS REALIZADOS</p>
			<br>
			<ol class="ol-none">
				<li><b>ESPACIOS DE VIVIENDA:</b> Residencial, medio, interés social y fraccionamientos urbanos. </li>

				<li><b>ESPACIOS COMERCIALES:</b> Tiendas departamentales, boutiques, agencias vehiculares, tiendas minoristas, complejos comerciales.</li>

				<li><b>ESPACIOS DE HOSPITALIDAD:</b> Centros turísticos, museos y centros culturales, centros de convenciones, hoteles, restaurantes, bares, cafés, teatros, cines, foros, arenas y centros de espectáculos.</li>

				<li><b>ESPACIOS PRODUCTIVOS Y EDUCATIVOS:</b> Oficinas, centros de salud, hospitales, bodegas y naves industriales, plantas, parques industriales, manufactureras, planteles educativos, bibliotecas.</li>

				<li><b>ESPACIOS PÚBLICOS:</b> Alumbrado público, puentes y túneles, estructuras, monumentos y fachadas, plazas y jardines públicos, estadios e instalaciones deportivas, aeropuertos, terminales. </li>
			</ol>
			<br>
			<p>PROYECTOS CONCEPTUALES</p>
			<br>
			<ol class="ol-none">
				<li><b>CONCEPTOS:</b> Propuestas de iluminación que no hayan llegado a la etapa de realización y cuyo objetivo sea resolver, mejorar o rescatar algún espacio interior o exterior.</li>
			</ol>
			<p>
				Las categorías anteriores se utilizan para organizar los proyectos elegibles y ayudar a los jueces en la revisión de los mismos. En caso de ser necesario, los organizadores se reservan el derecho de cambiar los proyectos de la categoría en la que fueron inscritos.
			</p>

			<h3 class="text-uppercase titles">CRITERIOS DE SELECCIÓN DE LOS JUECES</h3>
			<p>El proceso de evaluación de <b>Construlita Lighting Awards</b> se lleva a cabo gracias al compromiso de tres profesionales de reconocida trayectoria internacional, que tienen en común el entusiasmo por difundir la cultura de la luz y compartir sus conocimientos con otros profesionales. 
			<br>
			En cada edición, los jueces participan de manera voluntaria y destaca su espíritu de colaboración y compromiso con la disciplina del diseño de iluminación. El jurado debe siempre mantener una visión imparcial y objetiva en contribución al objetivo común de esta iniciativa. Los organizadores se reservan el derecho de invitar a más miembros del jurado en caso de ser necesario y en favor del adecuado desarrollo del evento.</p>
			<h3 class="text-uppercase titles">PUNTAJES, RECONOCIMIENTOS Y PREMIOS</h3>
			<p>Los proyectos dentro de cada categoría serán considerados por sus propios méritos y no se evaluarán el uno contra el otro. Los jueces calificarán cada proyecto bajo criterios de calidad, creatividad, solvencia técnica y buenas prácticas de diseño de iluminación, sin importar el tamaño ni el presupuesto del proyecto, otorgando hasta 5 estrellas por juez y una adicional a quien se considere merecedor del máximo galardón <b>Master Lighting</b>.</p>

			<p>Una categoría puede ser declarada como desierta, en caso de que ningún proyecto inscrito en ella alcance la puntuación requerida para hacerse acreedor a premiación, esto es más del 75% del total de estrellas que pueden otorgar los miembros del jurado.</p>

			<p>
				El máximo galardón de los <b>Construlita Lighting Awards</b> es el reconocimiento <b>Master Lighting</b> y será otorgado al proyecto que se considere el mejor en iluminación (entre todos los registrados en las categorías de Vivienda, Comercial, Hospitalidad, Espacios Productivos y Educativos y Espacios Públicos) por su excelencia y cuidado en el diseño de acuerdo con el puntaje más alto y el voto de calidad de los miembros del jurado. El ganador se hará acreedor a:
			</p>

			<ol class="ol-none">
				<li>
				Un tour pagado para asistir a LFI 2018. Incluye: viaje redondo en avión, tres noches de hospedaje (con desayuno y cena), entrada a dos días de la exposición y cinco entradas a seminarios, workshops y conferencias de su interés durante los días de exposición. Sujeto a la disponibilidad del ganador para viajar en la fecha en que se realiza el evento.
			</li>
			</ol>
			
			<p>
				El reconocimiento de <b>Excelencia en Iluminación</b> se otorgará de manera individual para cada una de las siguientes categorías: Vivienda, Comercial, Hospitalidad, Espacios Productivos y Educativos y Espacios Públicos. Serán galardonados los proyectos por su diseño estético y técnico de acuerdo con el puntaje más alto dentro de cada categoría, más el voto de calidad ejercido por los miembros del jurado.
			</p>
			<p>Los premios correspondientes consisten en:</p>
			<ol class="ol-none">
				<li>Una membresía anual a la Illuminating Engineering Society IES.</li>
				<li>Una beca de 100% para asistir a los seminarios IES - Sección México durante 2018.</li>
				<li>Una biblioteca de tres libros de iluminación o el Lighting Handbook 10ª edición.</li>
				<li>Un instrumento de escritura.</li>
			</ol>
			<p>El reconocimiento al <b>Sentido de la Luz</b> será otorgado al mejor proyecto de la categoría Conceptos, en la que se evaluarán aspectos de solvencia técnica y buenas prácticas de diseño de iluminación. El ganador de este reconocimiento recibirá como premio un luminario decorativo de <b>GRUPO CONSTRULITA</b>.</p>

			<p>
			Los participantes de <b>Construlita Lighting Awards</b> podrán ser acreedores a uno o más reconocimientos, mismos que no son intercambiables por su valor monetario o sustituibles por otros premios.
			</p>
			<h3 class="text-uppercase titles">
				REQUISITOS PARA REGISTRO DE PARTICIPANTES Y CARGA DE PROYECTOS
			</h3>
			<p>Materiales visuales</p>

			<p><b>Fotografía del equipo de diseñadores de iluminación</b></p>

			<p>Cantidad: 1 archivo (individual o grupal)</p>
			<p>Resolución: 300 - 600 dpi</p>
			<p>Dimensiones: 1380 x 670 pixeles, aproximado.</p>
			<p>Formato: .JPG o .PNG, sin excepción.</p>
			<p>Peso: Se recomienda un peso máximo de 7 MB por archivo.</p>

			<p><b>Fotografías del proyecto, renders o sketch</b></p>

			<p>Cantidad: mínimo 6 archivos, máximo 12 archivos</p>
			<p>Resolución: 300 - 600 dpi</p>
			<p>Dimensiones: 1380 x 670 pixeles, aproximado</p>
			<p>Formato: .JPG o .PNG, sin excepción.</p>
			<p>Peso: Se recomienda un peso máximo de 7 MB por archivo.</p>

			<p>Se requiere un mínimo de seis imágenes para cada proyecto y un máximo de doce. Los archivos de imagen deben estar libres de textos postproducción o retoques y deberán estar nombrados con el nombre del proyecto y numerados consecutivamente del 1 al 6. Deberá usarse el dígito 0 para la foto del equipo, con el nombre del equipo de diseño o bien, del autor del diseño de iluminación.</p>

			<p>Ej. HotelVistaHermosa_1.jpg, NombreDelEquipo_0.jpeg o bien, NombreDelConcursante_0.png</p>

			<p>Video (opcional)</p>

			<p>Se podrán adjuntar direcciones URL de videos para complementar la información de los proyectos. </p>
			<p>Si el proyecto incluye aplicaciones de luz dinámica, cambios de temperatura o color y/o atenuaciones, es OBLIGATORIO que se incluya un video en el que se puedan apreciar los efectos. El video debe estar libre de narraciones, música, subtítulos, edición o cualquier técnica que altere el resultado del efecto de iluminación logrado en el proyecto.</p>

