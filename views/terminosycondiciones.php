			cla/sesion" target="_blank">INICIAR SESIÓN</a>. Se deberá crear un nuevo usuario y aceptar los términos y condiciones.</p>

			<h3 class="text-uppercase titles">MÉTODOS DE REGISTRO DE PROYECTO(S)</h3>
			<p>El proceso de registro del proyecto se realizará exclusivamente en línea en nuestro <a href="http://www.construlitalighting.com/awards_2016/" target="_blank">sitio</a> dentro de la sección <a href="http://www.construlitalighting.com/
			cla/sesion" target="_blank">INICIAR SESIÓN</a> del menú principal. Es necesario ingresar el correo electrónico y contraseña con los que se registró el participante. Aparecerá el formulario que se deberá llenar con datos como: nombre del proyecto, categoría en la que concursa, ubicación, características técnicas y de diseño. También se podrán subir archivos de imágenes, incluir URL de videos y datos extra como catálogos de conceptos y planos. Además, se deberá redactar una descripción del proyecto con un máximo de 500 palabras, destacando los puntos que requieran primordial atención por parte de los jueces: la historia del espacio, el objetivo que se persiguió con el diseño y cuáles fueron las principales herramientas de iluminación que se utilizaron.
			</p>

			<p>
				Se podrán guardar los proyectos que desee mantener en la plataforma para posteriormente publicar el proyecto y que este sea enviado a evaluación. Cuando se publica el proyecto, éste pasará a formar parte de la lista de elegibilidad de proyectos. Un miembro del staff de <b>GRUPO CONSTRULITA</b> validará que el proyecto cumple con las condiciones de <b>Elegibilidad</b> descritas en la primera sección de este documento, una vez confirmado se enviará al escritorio digital de cada uno de los jueces para que emita su fallo sobre el proyecto.
			</p>

