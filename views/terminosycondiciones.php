			</p>
			<p>Solo se premiarán a los participantes que asistan al evento de Premiación y Reconocimientos, por lo que recomendamos realizar el pre-registro para la feria en <a href="www.expolightingamerica.com" target="_blank"></a>www.expolightingamerica.com.</p>
	
