                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse12">
                            ¿Cuáles son las características que deben tener las fotografías de mi proyecto?</a>
                          </h4>
                        </div>
                        <div id="collapse12" class="panel-collapse collapse">
                          <div class="panel-body"><li>Se permiten <b>8 MB como peso máximo</b> por cada fotografía.</li>
                          <li>Las fotografías deberán tener una <b>resolución mínima de 72 dpi.</b></li>
                          <li>Cada fotografía deberá tener una dimensión mínima a 720 px en alto y ancho.</b></li>   
                          <li>Únicamente se aceptan archivos en formato <b>.JPG y .PNG.</b></li>    
                          <li>También podrás incluir planos de tu proyecto de iluminación.</li>
                          <li> Todas las fotografías deben estar libres de textos, edición o retoques.</li>   
                          <li>No se aceptan bocetos o renders.</li>   
                          <li>Los archivos deberán estar nombrados con el nombre del proyecto y numerados consecutivamente del 1 al 8. Ej.: HotelVistaHermosa_1.jpg</li>                        
                          </div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse13">
                            ¿Puedo incluir un render o boceto?</a>
                          </h4>
                        </div>
                        <div id="collapse13" class="panel-collapse collapse">
                          <div class="panel-body">No se aceptan bocetos o renders. Además de las fotografías, podrás enviar también planos del proyecto de iluminación.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse14">
                            Tengo un video de mi proyecto, ¿puedo enviarlo?</a>
                          </h4>
                        </div>
                        <div id="collapse14" class="panel-collapse collapse">
                          <div class="panel-body"><li>Sí, podrás enviar la dirección URL de tu video, si este se encuentra alojado en alguna plataforma pública como YouTube, Vimeo o tu propia página web.</li>
                          <li>Si tu proyecto cuenta con iluminación dinámica o programable es necesario que incluyas el video para explicar los efectos de iluminación y su impacto en el espacio que resuelven.</li>
                          <li>No se podrán adjuntar archivos de video en la plataforma de registro.</li>
                          </div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse15">
                            ¿Cómo son evaluados los proyectos?</a>
                          </h4>
                        </div>
                        <div id="collapse15" class="panel-collapse collapse">
                          <div class="panel-body"><li>Todos los proyectos serán entregados a un panel independiente de jueces quienes <b>dictaminarán los mejores proyectos</b> que cumplan con las características requeridas para cada categoría.</li>
                          <li>Los proyectos serán considerados por sus <b>propios méritos</b> y no se evaluarán el uno contra el otro.</li>
                          <li>Los jueces evaluarán los siguientes criterios en cada proyecto: <b>concepto y objetivo funcional, selección y aplicación de luminarios, solución estética e innovación.</b></li>
                          </div>
                        </div>
                      </div>

                      <div class="panel panel-default">
