                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse11">
                            ¿Cómo registro mis proyectos?</a>
                          </h4>
                        </div>
                        <div id="collapse11" class="panel-collapse collapse">
                          <div class="panel-body"> <li>Debes ingresar en <a href="http://www.construlitalighting.com/cla/" target="_blank"><b>www.construlitalighting.com/cla/</a></b> con una cuenta de participante y encontrarás las opciones de registro de proyectos.</li>
                            <li>Te sugerimos revisar el documento de <a href="/cla/resources/page/images/descargables/ConstrulitaLightingAwards2019_Lineamientos.pdf" target="_blank" class="amarillo font-bold">lineamientos y recomendaciones</a> para que conozcas más sobre los materiales que debes preparar para registrar tus proyectos.</li>
                            <li>Si te has registrado en ediciones pasadas de CONSTRULITA LIGHTING AWARDS, puedes ingresar utilizando la misma cuenta. </li>                              
                          </div>
                        </div>
