                          </h4>
                        </div>
                        <div id="collapse10" class="panel-collapse collapse">
                          <div class="panel-body">La fecha límite para registrar y postular proyectos es el <b>viernes 01 de febrero de 2019 a las 11:59 PM </b>, hora de la Ciudad de México. No existe prórroga bajo ninguna circunstancia.</div>
