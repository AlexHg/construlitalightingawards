                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse1">
                            ¿Quién puede participar en CONSTRULITA LIGHTING AWARDS?</a>
                          </h4>
                        </div>
                        <div id="collapse1" class="panel-collapse collapse in">
                          <div class="panel-body"><li>Cualquier persona puede registrarse y postular un proyecto en nuestra plataforma para que sea evaluado por el jurado.</li>
                            <li>Podrán participar profesionales independientes, despachos de diseño, arquitectura, iluminación, áreas afines y estudiantes.</li>
                            <li>No está permitida la participación de empresas fabricantes de productos de iluminación.</li>
                            </div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse2">
                            Trabajo para un despacho y no tengo proyectos independientes, ¿puedo participar? 
                            </a>
                          </h4>
                        </div>
                        <div id="collapse2" class="panel-collapse collapse">
                          <div class="panel-body">Podrás registrar cualquier proyecto en el que hayas colaborado, siempre y cuando cuentes con la autorización para presentar el proyecto y hacer uso de los materiales visuales del mismo.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse3">
                            No soy diseñador de iluminación, pero realicé un proyecto de iluminación que me gustaría postular.</a>
                          </h4>
                        </div>
                        <div id="collapse3" class="panel-collapse collapse">
                          <div class="panel-body">Podrán participar profesionales independientes, despachos de diseño, arquitectura, iluminación, áreas afines y estudiantes.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse4">
                            Soy estudiante, ¿puedo participar?</a>
                          </h4>
                        </div>
                        <div id="collapse4" class="panel-collapse collapse">
                          <div class="panel-body">¡Claro! Puedes participar siempre y cuando tu proyecto haya sido ejecutado y no se encuentre en etapa conceptual.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse5">
                            ¿Qué tipo de proyectos puedo registrar?</a>
                          </h4>
                        </div>
                        <div id="collapse5" class="panel-collapse collapse">
                          <div class="panel-body">Podrás postular proyectos realizados durante 2017 ó 2018, con los que no hayas participado en alguna edición anterior de CONSTRULITA LIGHTING AWARDS y que pertenezcan a alguna de las siguientes categorías:<br><br>
                          <li><b>ESPACIOS DE VIVIENDA:</b> Buscamos proyectos en los que la iluminación mejore la habitabilidad de cualquier espacio residencial. También se incluyen fraccionamientos y conjuntos habitacionales.</li>
                          <li><b>ESPACIOS COMERCIALES:</b> Buscamos proyectos que potencien la finalidad comercial del espacio y mejoren la exhibición de productos. Esta categoría incluye tiendas minoristas y mayoristas como: tiendas especializadas, tiendas departamentales, tiendas de autoservicio, agencias vehiculares, fashion malls y otros complejos comerciales.</li>
                          <li><b>ESPACIOS DE HOSPITALIDAD:</b> Buscamos proyectos cuya iluminación genere atmósferas y ambientes confortables para espacios como: centros turísticos y culturales, museos, centros de convenciones y espectáculos, hoteles, restaurantes, bares, cafeterías, teatros, cines, foros y arenas.</li>
                          <li><b>ESPACIOS DE TRABAJO:</b> Buscamos proyectos en los que la iluminación facilite el desarrollo de actividades orientadas al servicio y la productividad. Esta categoría incluye: oficinas, centros de salud, hospitales, bodegas y naves industriales, plantas manufactureras, parques industriales, planteles educativos, edificios públicos y sucursales bancarias.</li>
                          <li><b>ESPACIOS PÚBLICOS:</b> Buscamos espacios exteriores cuya iluminación cubra una necesidad funcional específica, facilite la movilidad en cualquier escala o realce los atributos arquitectónicos y patrimoniales del espacio, inmueble o monumento que ilumina. Esta categoría incluye proyectos de alumbrado público, iluminación de puentes, túneles, estructuras, monumentos y fachadas, así como plazas y jardines públicos.</li>
                          <li><b>INSTALACIÓN ARTÍSTICA:</b> Buscamos piezas de iluminación que hayan sido exhibidas durante un periodo acotado de tiempo o formen parte de una colección específica. En esta categoría también se incluyen proyectos de iluminación escénica, iluminación de eventos e intervenciones lumínicas temporales.</li>
                          </div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse6">
                            Me gustaría postular un proyecto, pero al realizarlo no utilicé equipos de CONSTRULITA, ¿puedo participar?</a>
                          </h4>
                        </div>
                        <div id="collapse6" class="panel-collapse collapse">
                          <div class="panel-body">No es necesario que los equipos especificados en el proyecto sean de alguna marca de GRUPO CONSTRULITA. Se podrán registrar proyectos, aun cuando se hayan utilizado productos de otra marca.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse7">
                            ¿Cuántos proyectos puedo postular?</a>
                          </h4>
                        </div>
                        <div id="collapse7" class="panel-collapse collapse">
                          <div class="panel-body">Podrás registrar como máximo tres proyectos, sin importar la categoría en la que los registres.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse8">
                            ¿Puedo volver a concursar con un proyecto que presenté en una edición pasada de CONSTRULITA LIGHTING AWARDS?</a>
                          </h4>
                        </div>
                        <div id="collapse8" class="panel-collapse collapse">
                          <div class="panel-body">No son elegibles los proyectos que hayan concursado en ediciones anteriores.</div>
                        </div>
                      </div>

                      <div class="panel panel-default">
                        <div class="panel-heading">
                          <h4 class="panel-title">
                            <a data-toggle="collapse" data-parent="#accordion" href="#collapse9">
                            ¿Mi participación tiene algún costo?</a>
                          </h4>
                        </div>
                        <div id="collapse9" class="panel-collapse collapse">
                          <div class="panel-body"> <li>El registro en CONSTRULITA LIGHTING AWARDS es gratuito, tanto para participantes como para proyectos.</li>
                          <li>Recuerda que todos los participantes están invitados a acudir al evento de premiación dentro del marco de Expo Lighting America, el próximo 6 de marzo de 2019. Para ingresar a ELA debes realizar tu pre-registro sin costo en <a href="http://www.expolightingamerica.com" target="_blank"><b>www.expolightingamerica.com.</b></li> </div>
