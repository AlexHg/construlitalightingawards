						<h3>¿CUÁLES SON LOS REQUISITOS DE PRESENTACIÓN DE LAS IMÁGENES?</h3>
					</div>
					<ul>
						<li>Cantidad: mínimo 6 archivos, máximo 12 archivos.</li>
						<li>Resolución: 300 - 600 dpi, aproximado.</li>
						<li>Dimensiones: 1380 x 670 pixeles, aproximado.</li>
						<li>Formato: .JPG o .PNG, sin excepción.</li>
						<li>Peso: Se recomienda un peso máximo de 7 MB por archivo.</li>
						<li>Se requiere una imagen con la representación gráfica del proyecto que dé idea del concepto y las dimensiones del espacio intervenido (puede ser un boceto, render, sketch, dibujo a mano, etc.).</li>
						<li>Se deberá evitar el uso de luz de relleno en las fotografías.</li>
						<li>Incluir imágenes sin contribución de luz natural.</li>
					</ul>
